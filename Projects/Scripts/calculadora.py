#Calculator

def add(n1,n2):
    return n1+n2

def sub(n1,n2):
    return n1-n2

def mul(n1,n2):
    return n1*n2

def div(n1,n2):
    return n1/n2

operations = {
    "+": add,
    "-": sub,
    "*": mul,
    "/": div
}

def calculadora():
    print(logo)
    num1 = float(input("Enter the first number: " ))
    for op in operations:
        print(op)

    pode_continuar = True

    while pode_continuar:
        simbolo = input("Enter the operation: ")
        num2 = float(input("Enter the next number: " ))

        calcular_operacao = operations[simbolo]
        resposta = calcular_operacao(num1,num2)

        print(f"{num1} {simbolo} {num2} = {resposta}")

        if input(f"Type 'y' to continue calculating with {resposta} or 'n' to exit: ") == "y":
            num1 = resposta
        else:
            pode_continuar = False
            calculadora()

calculadora()
